# function that takes two parameters(numbers) and adds them together
def add(num1,num2):
  sum = num1 + num2
  return sum

# function that takes two parameters(numbers) and finds the difference
def subtract(num1,num2):
  diff = num1 - num2
  return diff

# function that takes two parameters(numbers) and finds the quotient
def divide(num1,num2):
  quot = num1/num2
  return quot

print(add(1, 2))
print(subtract(4, 1))
print(divide(8, 2))
